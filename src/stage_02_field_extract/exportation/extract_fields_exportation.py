def infer_doc_kind(filename: str, full_text: str) -> str:
    fn = (filename or "").lower()
    t = normalize_for_search(full_text)

    if ("certificate of origin" in t) or ("certificado de origem" in t) or ("certificat d'origine" in t):
        return "certificate_of_origin"

    # Draft BL: common anchors in your examples
    if ("bill of lading" in t) or ("bill of landing" in t) or ("b/lnumber" in t) or ("carrier'sreferencenumber" in t) or ("b/l number" in t):
        return "draft_bl"

    if "packing list" in fn or "packing" in fn or ("packing nr" in t) or ("packing list" in t):
        return "packing_list"

    if "commercial invoice" in fn or "invoice" in fn or ("invoice nr" in t) or ("factura comercial" in t) or ("fatura comercial" in t):
        return "commercial_invoice"

    if "dados cntr" in fn or ("booking:" in t and "container" in t and ("lacre" in t or "seal" in t)):
        return "container_data"

    return "unknown"


