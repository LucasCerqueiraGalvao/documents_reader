def now_iso() -> str:
    return datetime.now().strftime("%Y-%m-%dT%H:%M:%S")


def read_json(path: Path) -> dict:
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def write_json(path: Path, obj: dict) -> None:
    with path.open("w", encoding="utf-8") as f:
        json.dump(obj, f, ensure_ascii=False, indent=2)


def squish(s: str) -> str:
    s = (s or "").replace("\r", "\n")
    s = re.sub(r"[ \t]+", " ", s)
    s = re.sub(r"\n{3,}", "\n\n", s)
    return s.strip()


def normalize_for_search(s: str) -> str:
    return squish(s).lower()


def snippet_around(text: str, start: int, end: int, pad: int = 90) -> str:
    a = max(0, start - pad)
    b = min(len(text), end + pad)
    sn = text[a:b].replace("\n", " ")
    sn = re.sub(r"\s{2,}", " ", sn).strip()
    return sn


def parse_number_mixed(raw: str) -> Optional[float]:
    """
    Parse formats:
      - 133.780,00  -> 133780.00
      - 7,980.00    -> 7980.00
      - 29,922,400.- -> 29922400
      - 2100,000    -> 2100.0
    """
    if raw is None:
        return None
    s = str(raw).strip()
    if not s:
        return None

    s = s.replace("KGS", "").replace("KG", "").replace("US$", "").replace("$", "")
    s = s.replace(".-", "").replace(",-", "").replace("–", "-")
    s = re.sub(r"[^\d,.\-]", "", s)

    if not s:
        return None

    last_comma = s.rfind(",")
    last_dot = s.rfind(".")

    if last_comma != -1 and last_dot != -1:
        if last_comma > last_dot:
            # comma decimal, dot thousand
            s = s.replace(".", "")
            s = s.replace(",", ".")
        else:
            # dot decimal, comma thousand
            s = s.replace(",", "")
    elif last_comma != -1:
        # only commas -> assume decimal; if multiple commas, previous are thousand
        parts = s.split(",")
        if len(parts) > 2:
            s = "".join(parts[:-1]) + "." + parts[-1]
        else:
            s = s.replace(",", ".")
    elif last_dot != -1 and s.count(".") > 1:
        # many dots -> keep last as decimal
        parts = s.split(".")
        s = "".join(parts[:-1]) + "." + parts[-1]

    try:
        return float(s)
    except ValueError:
        return None


def parse_cnpj(raw: str) -> Optional[str]:
    if not raw:
        return None
    digits = re.sub(r"\D", "", raw)
    return digits if len(digits) == 14 else None


def parse_phone_candidates(text: str) -> List[str]:
    """
    Heuristic phone capture (BR + international)
    """
    phones = set()
    # +55 11 99999-9999, (11) 99999-9999, 11 99999-9999, +1 876-xxx-xxxx etc.
    patterns = [
        r"\+\d{1,3}\s*\(?\d{1,3}\)?\s*\d{3,5}[-\s]?\d{3,5}[-\s]?\d{0,5}",
        r"\(?\d{2,3}\)?\s*\d{4,5}[-\s]?\d{4}",
    ]
    for pat in patterns:
        for m in re.finditer(pat, text):
            ph = re.sub(r"\s{2,}", " ", m.group(0)).strip()
            if len(ph) >= 8:
                phones.add(ph)
    return sorted(phones)


def rx_first(text: str, pattern: str, flags=re.IGNORECASE | re.MULTILINE) -> Tuple[Optional[str], List[str], str]:
    m = re.search(pattern, text, flags)
    if not m:
        return None, [], "regex"
    val = m.group(1).strip() if m.lastindex else m.group(0).strip()
    ev = [snippet_around(text, m.start(), m.end())]
    return val, ev, "regex"


def rx_findall(text: str, pattern: str, flags=re.IGNORECASE | re.MULTILINE, limit_ev: int = 12) -> Tuple[List[str], List[str], str]:
    out = []
    ev = []
    for m in re.finditer(pattern, text, flags):
        val = m.group(1).strip() if m.lastindex else m.group(0).strip()
        out.append(val)
        if len(ev) < limit_ev:
            ev.append(snippet_around(text, m.start(), m.end()))
    return out, ev, "regex_findall"


def line_after_label(text: str, label_pattern: str, max_lookahead: int = 6) -> Tuple[Optional[str], List[str], str]:
    """
    Find a line matching label_pattern and return the next non-empty line.
    """
    lines = [ln.rstrip() for ln in text.splitlines()]
    for i, ln in enumerate(lines):
        if re.search(label_pattern, ln, re.IGNORECASE):
            for j in range(i + 1, min(i + 1 + max_lookahead, len(lines))):
                nxt = lines[j].strip()
                if nxt:
                    return nxt, [f"{ln.strip()} -> {nxt}"], "line_heuristic"
    return None, [], "line_heuristic"


def inline_after_label(text: str, label_pattern: str) -> Tuple[Optional[str], List[str], str]:
    """
    Find a line matching label_pattern and capture text after it on same line.
    """
    lines = [ln.rstrip() for ln in text.splitlines()]
    for ln in lines:
        m = re.search(label_pattern, ln, re.IGNORECASE)
        if m:
            after = ln[m.end():].strip(" :\t-")
            if after:
                return after, [ln.strip()], "inline_heuristic"
    return None, [], "inline_heuristic"


def make_field(required: bool, value: Any, evidence: List[str], method: str) -> Dict[str, Any]:
    present = value is not None and value != "" and value != [] and value is not False
    return {
        "present": bool(present),
        "required": bool(required),
        "value": value if present else None,
        "evidence": evidence if present else [],
        "method": method
    }


def join_pages(stage01: dict) -> str:
    parts: List[str] = []
    for p in stage01.get("pages", []):
        page_no = p.get("page")
        txt = (p.get("text") or "").strip()
        if txt:
            parts.append(f"[PAGE {page_no}] {txt}")
    return squish("\n\n".join(parts))


