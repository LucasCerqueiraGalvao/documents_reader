def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--input", required=True, help="Pasta com *_extracted.json do Stage 01 (exportation)")
    ap.add_argument("--output", required=True, help="Pasta de saída do Stage 02 (fields) (exportation)")
    args = ap.parse_args()

    in_dir = Path(args.input)
    out_dir = Path(args.output)
    out_dir.mkdir(parents=True, exist_ok=True)

    extracted_files = sorted(in_dir.glob("*_extracted.json"))
    if not extracted_files:
        raise SystemExit(f"Nenhum *_extracted.json encontrado em: {in_dir}")

    summary_items: List[dict] = []

    for p in extracted_files:
        stage01 = read_json(p)
        original_pdf = stage01.get("file") or p.name.replace("_extracted.json", ".pdf")

        full_text = join_pages(stage01)
        doc_kind = infer_doc_kind(original_pdf, full_text)

        fields, warns = extract_by_kind(doc_kind, full_text)

        out_obj = build_output(p.name, original_pdf, doc_kind, fields, warns)
        out_path = out_dir / p.name.replace("_extracted.json", "_fields.json")
        write_json(out_path, out_obj)

        summary_items.append({
            "file": original_pdf,
            "stage01": p.name,
            "doc_kind": doc_kind,
            "missing_required_fields": out_obj["missing_required_fields"],
            "warnings": out_obj["warnings"]
        })

    write_json(out_dir / "_stage02_summary.json", {
        "generated_at": now_iso(),
        "flow": "exportation",
        "items": summary_items
    })

    print("Concluído.")


if __name__ == "__main__":
    main()
