def ex_commercial_invoice(text: str) -> Tuple[Dict[str, Dict[str, Any]], List[str]]:
    warnings: List[str] = []
    fields: Dict[str, Dict[str, Any]] = {}

    inv_no, ev, m = line_after_label(text, r"^invoice\s*nr\.?$")
    if not inv_no:
        inv_no, ev, m = inline_after_label(text, r"invoice\s*nr\.?")
    fields["invoice_number"] = make_field(True, inv_no, ev, m)

    date, ev, m = rx_first(text, r"\b(\d{2}/\d{2}/\d{4})\b")
    fields["invoice_date"] = make_field(True, date, ev, m)

    country, ev, m = line_after_label(text, r"^pais\s+de\s+origen\b")
    fields["country_of_origin"] = make_field(True, country, ev, m)

    mode, ev, m = line_after_label(text, r"^via\s+de\s+transporte\b")
    fields["transport_mode"] = make_field(True, mode, ev, m)

    # Ports often appear in header line: "Puerto de Embarque Puerto de Destino"
    pol, ev1, m1 = inline_after_label(text, r"puerto\s+de\s+embarque\s+puerto\s+de\s+destino")
    port_loading = None
    port_destination = None
    if pol:
        # try: split last tokens
        tokens = pol.split()
        # Example: "Maritimo SANTOS PORT KINGSTON"
        # choose last 3/2 tokens as best-effort
        if len(tokens) >= 2:
            # find KINGSTON-like last token
            port_destination = tokens[-1]
            # loading could be last 2 or 3 tokens before destination
            port_loading = " ".join(tokens[-3:-1]) if len(tokens) >= 3 else tokens[0]
    fields["port_of_loading"] = make_field(True, port_loading, ev1 if port_loading else [], m1)
    fields["port_of_destination"] = make_field(True, port_destination, ev1 if port_destination else [], m1)

    gross_raw, ev, m = rx_first(text, r"peso\s+bruto\s*\(kg\)\s*[\r\n ]*([0-9\.,]+)")
    fields["gross_weight_kg"] = make_field(True, parse_number_mixed(gross_raw) if gross_raw else None, ev, m)

    net_raw, ev, m = rx_first(text, r"peso\s+neto\s*\(kg\)\s*[\r\n ]*([0-9\.,]+)")
    fields["net_weight_kg"] = make_field(True, parse_number_mixed(net_raw) if net_raw else None, ev, m)

    inc, ev, m = rx_first(text, r"\bincoterms\b.*?\b([A-Z]{3})\b", flags=re.IGNORECASE | re.DOTALL)
    fields["incoterm"] = make_field(True, inc, ev, m)

    curr, ev, m = line_after_label(text, r"^moneda\b")
    if not curr:
        curr, ev, m = rx_first(text, r"\bcurrency\b\s*[:\-]?\s*([A-Z]{3})\b")
    fields["currency"] = make_field(True, curr, ev, m)

    ncm, ev, m = rx_first(text, r"\bn\.c\.m\s*([0-9\.]+)")
    if not ncm:
        ncm, ev, m = rx_first(text, r"\bNCM\b\s*[:\-]?\s*([0-9\.]{6,})")
    fields["ncm"] = make_field(True, ncm, ev, m)

    cntr, ev, m = rx_first(text, r"\bCNTR\b\s*[\r\n ]*(\d+)\b")
    fields["container_count"] = make_field(True, int(cntr) if cntr else None, ev, m)

    # Exporter CNPJ
    cnpj_raw, ev, m = rx_first(text, r"\bC\.?N\.?P\.?J\.?\s*[:\-]?\s*([0-9\.\-\/]+)")
    fields["exporter_cnpj"] = make_field(True, parse_cnpj(cnpj_raw), ev, m)

    # Exporter name: heuristic - first long uppercase line (common in invoices)
    exporter, ev, m = rx_first(text, r"\n([A-Z0-9][A-Z0-9 &\.\-]{6,})\n", flags=re.MULTILINE)
    fields["exporter_name"] = make_field(True, exporter, ev, m)

    # Importer name: look for Consignee block
    importer, ev, m = inline_after_label(text, r"\bconsignee\b")
    if not importer:
        importer, ev, m = rx_first(text, r"\bConsignee\b\s*[:\-]?\s*([A-Z0-9 &\.\-]{3,})", flags=re.IGNORECASE)
    fields["importer_name"] = make_field(True, importer, ev, m)

    # Payment terms (not mandatory in your export list, but useful)
    pay, ev, m = rx_first(text, r"\bcobranza\b.*", flags=re.IGNORECASE)
    fields["payment_terms"] = make_field(False, pay, ev, m)

    return fields, warnings


def ex_packing_list(text: str) -> Tuple[Dict[str, Dict[str, Any]], List[str]]:
    warnings: List[str] = []
    fields: Dict[str, Dict[str, Any]] = {}

    pk_no, ev, m = line_after_label(text, r"^packing\s*nr\.?$")
    if not pk_no:
        pk_no, ev, m = inline_after_label(text, r"packing\s*nr\.?")
    fields["packing_list_number"] = make_field(True, pk_no, ev, m)

    date, ev, m = rx_first(text, r"\b(\d{2}/\d{2}/\d{4})\b")
    fields["packing_date"] = make_field(True, date, ev, m)

    gross_raw, ev, m = rx_first(text, r"peso\s+bruto\s*\(kg\)\s*[\r\n ]*([0-9\.,]+)")
    fields["gross_weight_kg"] = make_field(True, parse_number_mixed(gross_raw) if gross_raw else None, ev, m)

    net_raw, ev, m = rx_first(text, r"peso\s+neto\s*\(kg\)\s*[\r\n ]*([0-9\.,]+)")
    fields["net_weight_kg"] = make_field(True, parse_number_mixed(net_raw) if net_raw else None, ev, m)

    ncm, ev, m = rx_first(text, r"\bn\.c\.m\s*([0-9\.]+)")
    fields["ncm"] = make_field(True, ncm, ev, m)

    inc, ev, m = rx_first(text, r"\bincoterms\b.*?\b([A-Z]{3})\b", flags=re.IGNORECASE | re.DOTALL)
    fields["incoterm"] = make_field(True, inc, ev, m)

    cntr, ev, m = rx_first(text, r"\bCNTR\b\s*[\r\n ]*(\d+)\b")
    fields["container_count"] = make_field(True, int(cntr) if cntr else None, ev, m)

    # Containers table lines (best-effort):
    containers: List[Dict[str, Any]] = []
    evs: List[str] = []
    for ln in text.splitlines():
        s = re.sub(r"\s{2,}", " ", ln.strip())
        # Example: TEMU1680211 M1024218 76,98
        m1 = re.match(r"^([A-Z]{4}\d{7})\s+([A-Z0-9]{6,})\s+([\d\.,]+)\b", s)
        if m1:
            containers.append({
                "container_number": m1.group(1),
                "seal": m1.group(2),
                "m2": parse_number_mixed(m1.group(3)),
                "raw_line": s
            })
            if len(evs) < 10:
                evs.append(s)

    fields["containers"] = make_field(True, containers if containers else None, evs, "line_parse")

    return fields, warnings


def parse_bl_container_lines(text: str) -> List[Dict[str, Any]]:
    """
    Parse BL draft container rows like:
      BMOU1157130 M1017120 2100,000 20 Dry 971 BOX, CARTON 26840,000 15,510
    """
    items: List[Dict[str, Any]] = []
    for ln in text.splitlines():
        s = re.sub(r"\s{2,}", " ", ln.strip())
        # container seal tara type packages ... gross measurement
        m = re.match(
            r"^([A-Z]{4}\d{7})\s+([A-Z0-9]+)\s+([\d\.,]+)\s+(\d+\s*Dry|\d+\s*HC|\d+\s*GP|\d+\s*RF|\d+\s*DV)\s+(\d+)\s+BOX.*?\s+([\d\.,]+)\s+([\d\.,]+)$",
            s,
            re.IGNORECASE
        )
        if m:
            items.append({
                "container_number": m.group(1),
                "seal": m.group(2),
                "tara_kg": parse_number_mixed(m.group(3)),
                "container_type": m.group(4),
                "packages": int(m.group(5)),
                "gross_weight_kg": parse_number_mixed(m.group(6)),
                "cbm_or_measurement": parse_number_mixed(m.group(7)),
                "raw_line": s
            })
    return items


def incoterm_freight_expectation(incoterm: str) -> Optional[str]:
    """
    Very simple heuristic (not a law):
    - C-terms (CFR/CIF/CPT/CIP) usually PREPAID
    - D-terms (DAP/DDP/DPU) usually PREPAID
    - E/F terms can be COLLECT (often) but can be prepaid too.
    """
    if not incoterm:
        return None
    inc = incoterm.upper().strip()
    if inc.startswith("C") or inc.startswith("D"):
        return "PREPAID"
    if inc in {"EXW", "FCA", "FOB", "FAS"}:
        return "COLLECT"
    return None


def ex_draft_bl(text: str) -> Tuple[Dict[str, Dict[str, Any]], List[str]]:
    warnings: List[str] = []
    fields: Dict[str, Dict[str, Any]] = {}

    # Freight mode
    freight, ev, m = rx_first(text, r"FREIGHT\s+(PREPAID|COLLECT)", flags=re.IGNORECASE)
    freight_mode = freight.upper() if freight else None
    fields["freight_mode"] = make_field(True, freight_mode, ev, m)

    # Incoterm
    inc, ev2, m2 = rx_first(text, r"INCOTERM:\s*([A-Z]{3})", flags=re.IGNORECASE)
    fields["incoterm"] = make_field(True, inc, ev2, m2)

    # Compatibility warning (optional)
    if freight_mode and inc:
        expected = incoterm_freight_expectation(inc)
        if expected and expected != freight_mode:
            warnings.append(f"Possível inconsistência: incoterm {inc} normalmente sugere {expected}, mas BL está {freight_mode}.")

    # NCM / HS
    ncm, ev, m = rx_first(text, r"\bNCM(?:/NALADI)?[:\s]+([0-9\.]{6,})", flags=re.IGNORECASE)
    if not ncm:
        ncm, ev, m = rx_first(text, r"\bNCM:\s*(\d{8})\b", flags=re.IGNORECASE)
    fields["ncm"] = make_field(True, ncm, ev, m)

    # DU-E
    due, ev, m = rx_first(text, r"\bDUE:\s*([A-Z0-9\.\-\/]+)", flags=re.IGNORECASE)
    if due and due.strip() == ".":
        due = None
        ev = []
    fields["due"] = make_field(True, due, ev, m)

    # RUC
    ruc, ev, m = rx_first(text, r"\bRUC:\s*([A-Z0-9]+)", flags=re.IGNORECASE)
    fields["ruc"] = make_field(True, ruc, ev, m)

    # Booking (useful)
    booking, ev, m = rx_first(text, r"\b(SSZ\d{6,})\b")
    fields["booking_number"] = make_field(False, booking, ev, m)

    # Wooden packing
    wood, ev, m = rx_first(text, r"WOODEN\s+PACKAGE\s*:\s*([A-Z \/\-]+)", flags=re.IGNORECASE)
    fields["wooden_packing"] = make_field(True, wood, ev, m)

    # Containers list
    containers = parse_bl_container_lines(text)
    evc = [c["raw_line"] for c in containers[:10]] if containers else []
    fields["containers"] = make_field(True, containers if containers else None, evc, "line_parse")

    # Totals (cartons/packages and weights)
    cartons_raw, ev, m = rx_first(text, r"([\d\.,]+)\s+CARTONS", flags=re.IGNORECASE)
    cartons_val = None
    if cartons_raw:
        n = parse_number_mixed(cartons_raw)
        cartons_val = int(n) if n is not None else None
    fields["total_cartons"] = make_field(True, cartons_val, ev, m)

    net_raw, ev, m = rx_first(text, r"Net\s*Weight:\s*([\d\.,]+)\s*kg", flags=re.IGNORECASE)
    fields["net_weight_kg_total"] = make_field(True, parse_number_mixed(net_raw) if net_raw else None, ev, m)

    gross_raw, ev, m = rx_first(text, r"Gross\s*Weight:\s*([\d\.,]+)\s*kg", flags=re.IGNORECASE)
    fields["gross_weight_kg_total"] = make_field(True, parse_number_mixed(gross_raw) if gross_raw else None, ev, m)

    m3_raw, ev, m = rx_first(text, r"\b([\d\.,]+)\s*m³\b", flags=re.IGNORECASE)
    fields["cubic_meters_total"] = make_field(True, parse_number_mixed(m3_raw) if m3_raw else None, ev, m)

    # Exporter CNPJ / Exporter contact
    cnpj_raw, ev, m = rx_first(text, r"\bCNPJ:\s*([0-9\.\-\/]+)", flags=re.IGNORECASE)
    fields["exporter_cnpj"] = make_field(True, parse_cnpj(cnpj_raw), ev, m)

    # Basic "shipper" name heuristic
    shipper, ev, m = rx_first(text, r"\bShipper\b.*?\n([A-Z0-9 &\.\-]{4,})\n", flags=re.IGNORECASE | re.DOTALL)
    fields["exporter_name"] = make_field(True, shipper, ev, m)

    # Try phone/contact: extract any phone-like patterns from BL
    phones = parse_phone_candidates(text)
    fields["phones_found"] = make_field(False, phones if phones else None, phones[:8] if phones else [], "phone_heuristic")

    # Importer/Notify contacts (best-effort)
    importer, ev, m = rx_first(text, r"\bConsignee\b.*?\n([A-Z0-9 &\.\-]{4,})\n", flags=re.IGNORECASE | re.DOTALL)
    fields["importer_name"] = make_field(True, importer, ev, m)

    notify, ev, m = rx_first(text, r"\bNotify Party\b.*?\n([A-Z0-9 &\.\-]{4,})\n", flags=re.IGNORECASE | re.DOTALL)
    fields["notify_party_name"] = make_field(True, notify, ev, m)

    return fields, warnings


def ex_certificate_of_origin(text: str) -> Tuple[Dict[str, Dict[str, Any]], List[str]]:
    warnings: List[str] = []
    fields: Dict[str, Dict[str, Any]] = {}

    inv, ev, m = rx_first(text, r"\b(I-\d{4}\/\d{2})\b")
    fields["invoice_number"] = make_field(True, inv, ev, m)

    date, ev, m = rx_first(text, r"\b(\d{2}/\d{2}/\d{4})\b")
    fields["certificate_date"] = make_field(True, date, ev, m)

    mode, ev, m = rx_first(text, r"\bBy Sea\b", flags=re.IGNORECASE)
    fields["transport_mode"] = make_field(True, "By Sea" if mode else None, ev, "regex")

    exporter, ev, m = rx_first(text, r"\bExporter\b.*?\n([A-Z0-9 &\.\-]{4,})\n", flags=re.IGNORECASE | re.DOTALL)
    fields["exporter_name"] = make_field(True, exporter, ev, m)

    importer, ev, m = rx_first(text, r"\bConsignee\b.*?\n([A-Z0-9 &\.\-]{4,})\n", flags=re.IGNORECASE | re.DOTALL)
    fields["importer_name"] = make_field(True, importer, ev, m)

    # KGS: may appear twice (net/gross). Pick min as net and max as gross.
    all_kgs, evs, mm = rx_findall(text, r"([\d\.,]+)\s*KGS", flags=re.IGNORECASE)
    nums = [parse_number_mixed(x) for x in all_kgs]
    nums = [x for x in nums if x is not None]
    if len(nums) >= 2:
        fields["net_weight_kg"] = make_field(True, min(nums), evs[:1], "regex_findall")
        fields["gross_weight_kg"] = make_field(True, max(nums), evs[:2], "regex_findall")
    else:
        fields["net_weight_kg"] = make_field(True, None, [], "regex_findall")
        fields["gross_weight_kg"] = make_field(True, None, [], "regex_findall")

    m2_raw, ev, m = rx_first(text, r"([\d\.,]+)\s*m²", flags=re.IGNORECASE)
    fields["total_m2"] = make_field(True, parse_number_mixed(m2_raw) if m2_raw else None, ev, m)

    return fields, warnings


def ex_container_data(text: str) -> Tuple[Dict[str, Dict[str, Any]], List[str]]:
    warnings: List[str] = []
    fields: Dict[str, Dict[str, Any]] = {}

    inv, ev, m = rx_first(text, r"FATURA:\s*([A-Z0-9\-\/]+)", flags=re.IGNORECASE)
    fields["invoice_number"] = make_field(True, inv, ev, m)

    booking, ev, m = rx_first(text, r"BOOKING:\s*(SSZ\d+)", flags=re.IGNORECASE)
    fields["booking_number"] = make_field(True, booking, ev, m)

    # Container rows:
    # "1 TEMU1680211 M1024218 2100 30480"
    containers: List[Dict[str, Any]] = []
    evs: List[str] = []
    for ln in text.splitlines():
        s = re.sub(r"\s{2,}", " ", ln.strip())
        m1 = re.match(r"^\d+\s+([A-Z]{4}\d{7})\s+([A-Z0-9]{6,})\s+([\d\.,]+)\s+(\d+)\b.*$", s)
        if m1:
            containers.append({
                "container_number": m1.group(1),
                "seal": m1.group(2),
                "tara_kg": parse_number_mixed(m1.group(3)),
                "mgw": parse_number_mixed(m1.group(4)),
                "raw_line": s
            })
            if len(evs) < 12:
                evs.append(s)

    fields["containers"] = make_field(True, containers if containers else None, evs, "line_parse")

    return fields, warnings


def extract_by_kind(doc_kind: str, full_text: str) -> Tuple[Dict[str, Dict[str, Any]], List[str]]:
    if doc_kind == "commercial_invoice":
        return ex_commercial_invoice(full_text)
    if doc_kind == "packing_list":
        return ex_packing_list(full_text)
    if doc_kind == "draft_bl":
        return ex_draft_bl(full_text)
    if doc_kind == "certificate_of_origin":
        return ex_certificate_of_origin(full_text)
    if doc_kind == "container_data":
        return ex_container_data(full_text)
    return {}, []


# ---------------------------
# Output format
# ---------------------------

def build_output(stage01_filename: str, original_pdf: str, doc_kind: str,
                 fields: Dict[str, Dict[str, Any]], warnings: List[str]) -> dict:
    missing = [k for k, v in fields.items() if v.get("required") and not v.get("present")]
    return {
        "source": {
            "stage01_file": stage01_filename,
            "original_file": original_pdf,
            "doc_kind": doc_kind
        },
        "generated_at": now_iso(),
        "fields": fields,
        "missing_required_fields": missing,
        "warnings": warnings or []
    }


